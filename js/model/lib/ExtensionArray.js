

Array.prototype.groupby = function (keyGetter) {
    const map = new Map();
    this.forEach((item) => {
      const key = keyGetter(item);
      const collection = map.get(key);
      if (!collection) {
        map.set(key, [item]);
      } else {
        collection.push(item);
      }
    });
    return map;
}